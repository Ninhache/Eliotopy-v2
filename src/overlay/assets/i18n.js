let currentLang = "en";

function t(key) {
    const dict = I18N[currentLang] || I18N.en;
    if (dict[key] !== undefined) return dict[key];
    if (I18N.en[key] !== undefined) return I18N.en[key];
    return key;
}

function applyI18n(lang) {
    if (lang && I18N[lang]) currentLang = lang;
    document.querySelectorAll("[data-i18n]").forEach(node => {
        node.textContent = t(node.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-tip]").forEach(node => {
        node.setAttribute("data-tip", t(node.getAttribute("data-i18n-tip")));
    });
    if (typeof onI18nApplied === "function") onI18nApplied();
}
